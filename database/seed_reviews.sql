        WHEN rating <= 2 THEN 'Bad (1-2 stars)'
        ELSE 'Neutral (3 stars)'
    END AS ReviewType,
    COUNT(*) AS Count,
    CAST(COUNT(*) * 100.0 / (SELECT COUNT(*) FROM Reviews) AS DECIMAL(5,2)) AS Percentage
FROM Reviews
GROUP BY 
    CASE 
        WHEN rating >= 4 THEN 'Good (4-5 stars)'
        WHEN rating <= 2 THEN 'Bad (1-2 stars)'
        ELSE 'Neutral (3 stars)'
    END
ORDER BY Count DESC;

